const SPRITES = {
  player: { sprite: SPRITE_PLAYER, palette: PALETTE_PLAYER },
  human:  { sprite: SPRITE_HUMAN,  palette: PALETTE_HUMAN  },
  elf:    { sprite: SPRITE_ELF,    palette: PALETTE_ELF    },
  dwarf:  { sprite: SPRITE_DWARF,  palette: PALETTE_DWARF  },
  goblin: { sprite: SPRITE_GOBLIN, palette: PALETTE_GOBLIN },
  lizard: { sprite: SPRITE_LIZARD, palette: PALETTE_LIZARD },
  ogre:   { sprite: SPRITE_OGRE,   palette: PALETTE_OGRE   },
};

// HP background: green / yellow / red
const _HP_BG = { g: '#1a5c1a', y: '#7a6a00', r: '#7a1a1a' };

function _hpBucket(hpRatio) {
  return hpRatio > 0.5 ? 'g' : hpRatio > 0.25 ? 'y' : 'r';
}

// Lazily built OffscreenCanvas per (type × HP bucket) — 7 × 3 = 21 max
const _spriteCache = new Map();

function _getSpriteCanvas(type, hpRatio) {
  const bucket = _hpBucket(hpRatio);
  const key    = `${type}_${bucket}`;
  if (_spriteCache.has(key)) return _spriteCache.get(key);

  const def  = SPRITES[type];
  const rows = def.sprite.length;
  const cols = def.sprite[0].length;
  const oc   = new OffscreenCanvas(cols, rows);
  const oc2d = oc.getContext('2d');

  oc2d.fillStyle = _HP_BG[bucket];
  oc2d.fillRect(0, 0, cols, rows);

  for (let r = 0; r < rows; r++) {
    for (let c = 0; c < cols; c++) {
      const idx = def.sprite[r][c];
      if (idx === 0) continue;
      oc2d.fillStyle = def.palette[idx];
      oc2d.fillRect(c, r, 1, 1);
    }
  }

  _spriteCache.set(key, oc);
  return oc;
}

// Draw dot-art sprite onto the main canvas at (x, y) scaled to `size` px
// ctx is the global canvas context defined in main.js
function drawSpriteAt(type, hpRatio, x, y, size) {
  ctx.drawImage(_getSpriteCanvas(type, hpRatio), x, y, size, size);
}
